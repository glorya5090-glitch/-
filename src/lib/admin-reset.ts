function findNearestPackageRoot(startPath: string): string | null {
  let current = path.resolve(startPath);
  let candidate = fs.statSync(current, { throwIfNoEntry: false })?.isDirectory()
    ? current
    : path.dirname(current);

  while (true) {
    if (fs.existsSync(path.join(candidate, 'package.json'))) {
      return candidate;
    }
    const parent = path.dirname(candidate);
    if (parent === candidate) {
      return null;
    }
    candidate = parent;
  }
}

function resolveCurrentGlobalNpmCliInstallPath(): string | null {
  const scriptPath = process.argv[1];
  if (!scriptPath) {
    return null;
  }

  const packageRoot = findNearestPackageRoot(scriptPath);
  if (!packageRoot) {
    return null;
  }

  try {
    const parsed = JSON.parse(fs.readFileSync(path.join(packageRoot, 'package.json'), 'utf8')) as {
      name?: unknown;
    };
    if (parsed.name !== GLOBAL_NPM_CLI_PACKAGE_NAME) {
      return null;
    }
  } catch {
    return null;
  }

  const marker = `${path.sep}node_modules${path.sep}`;
  if (!packageRoot.includes(marker)) {
    return null;
  }

  const npmRootResult = spawnSync('npm', ['root', '-g'], { encoding: 'utf8' });
  if (npmRootResult.error) {
    throw new Error(
      `failed to inspect the ${LEGACY_GLOBAL_NPM_CLI_LABEL}: ${renderError(npmRootResult.error)}`,
    );
  }
  if (npmRootResult.status !== 0) {
    throw new Error(
      npmRootResult.stderr.trim()
        || npmRootResult.stdout.trim()
        || `failed to inspect the ${LEGACY_GLOBAL_NPM_CLI_LABEL} (exit code ${npmRootResult.status})`,
    );
  }

  const npmRoot = npmRootResult.stdout.trim();
  if (!npmRoot) {
    throw new Error(
      `failed to inspect the ${LEGACY_GLOBAL_NPM_CLI_LABEL}: npm root -g returned no path`,
    );
  }

  const expectedInstallPath = path.join(npmRoot, ...GLOBAL_NPM_CLI_PACKAGE_NAME.split('/'));
  return path.resolve(packageRoot) === path.resolve(expectedInstallPath) ? expectedInstallPath : null;
}

function uninstallCurrentGlobalNpmCli(): GlobalCliUninstallResult {
  const installPath = resolveCurrentGlobalNpmCliInstallPath();
  if (!installPath) {
    return {
      packageName: GLOBAL_NPM_CLI_PACKAGE_NAME,
      packageManager: 'npm',
      attempted: false,
      removed: false,
      installPath: null,
      skippedReason: 'current command is not running from a legacy global npm install',
    };
  }

  const uninstallResult = spawnSync(
    'npm',
    ['uninstall', '--global', GLOBAL_NPM_CLI_PACKAGE_NAME],
    { encoding: 'utf8' },
  );
  if (uninstallResult.error) {
    throw new Error(
      `failed to uninstall the ${LEGACY_GLOBAL_NPM_CLI_LABEL}: ${renderError(uninstallResult.error)}`,
    );
  }
  if (uninstallResult.status !== 0) {
    throw new Error(
      uninstallResult.stderr.trim()
        || uninstallResult.stdout.trim()
        || `failed to uninstall the ${LEGACY_GLOBAL_NPM_CLI_LABEL} (exit code ${uninstallResult.status})`,
    );
  }

  return {
    packageName: GLOBAL_NPM_CLI_PACKAGE_NAME,
    packageManager: 'npm',
    attempted: true,
    removed: true,
    installPath,
    skippedReason: null,
  };
}

