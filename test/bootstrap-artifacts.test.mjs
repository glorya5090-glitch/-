test('cleanupAutoGeneratedBootstrapArtifacts deletes recognized bootstrap files by default', async () => {
  const bootstrapArtifacts = await loadModule(`${Date.now()}-cleanup-delete`);
  const tempRoot = fs.mkdtempSync(path.join(os.tmpdir(), 'agentpay-bootstrap-artifacts-'));
  fs.chmodSync(tempRoot, 0o700);

  const plaintextPath = path.join(tempRoot, 'bootstrap-100-200.json');
  const redactedPath = path.join(tempRoot, 'bootstrap-300-400.json');
  const invalidPath = path.join(tempRoot, 'bootstrap-500-600.json');

  writePrivateJsonFile(plaintextPath, bootstrapPayload());
  writePrivateJsonFile(redactedPath, bootstrapPayload({
    agent_auth_token: '<redacted>',
    agent_auth_token_redacted: true
  }));
  writePrivateJsonFile(invalidPath, { hello: 'world' });

  const result = bootstrapArtifacts.cleanupAutoGeneratedBootstrapArtifacts('deleted', {
    resolveAgentPayHome: () => tempRoot
  });

  assert.equal(result.action, 'deleted');
  assert.deepEqual(
    result.files.map((entry) => [path.basename(entry.path), entry.cleanup]),
    [
      ['bootstrap-500-600.json', 'skipped'],
      ['bootstrap-300-400.json', 'deleted'],
      ['bootstrap-100-200.json', 'deleted']
    ]
  );
  assert.equal(fs.existsSync(plaintextPath), false);
  assert.equal(fs.existsSync(redactedPath), false);
  assert.equal(fs.existsSync(invalidPath), true);
});

