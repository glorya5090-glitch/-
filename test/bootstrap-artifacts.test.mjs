import test from 'node:test';
import assert from 'node:assert/strict';
import fs from 'node:fs';
import os from 'node:os';
import path from 'node:path';

const modulePath = new URL('../src/lib/bootstrap-artifacts.ts', import.meta.url);

function loadModule(caseId) {
  return import(modulePath.href + `?case=${caseId}`);
}

function writePrivateJsonFile(targetPath, payload) {
  fs.mkdirSync(path.dirname(targetPath), { recursive: true, mode: 0o700 });
  fs.writeFileSync(targetPath, JSON.stringify(payload, null, 2) + '\n', {
    encoding: 'utf8',
    mode: 0o600
  });
  fs.chmodSync(targetPath, 0o600);
}

function bootstrapPayload(overrides = {}) {
  const payload = {
    state_file: 'daemon_socket:/tmp/agentpay.sock',
    lease_id: 'lease-123',
    lease_expires_at: '2099-01-01T00:00:00Z',
    per_tx_policy_id: 'policy-per-tx',
    daily_policy_id: 'policy-daily',
    weekly_policy_id: 'policy-weekly',
    gas_policy_id: 'policy-gas',
    per_tx_max_wei: '1000000000000000000',
    daily_max_wei: '5000000000000000000',
    weekly_max_wei: '20000000000000000000',
    max_gas_per_chain_wei: '1000000000000000',
    vault_key_id: 'vault-key-123',
    vault_public_key: '03abcdef',
    agent_key_id: '00000000-0000-0000-0000-000000000001',
    agent_auth_token: 'secret-agent-token',
    agent_auth_token_redacted: false,
    network_scope: 'all networks',
    asset_scope: 'all assets',
    recipient_scope: 'all recipients',
    policy_attachment: 'policy_set',
    attached_policy_ids: undefined,
    policy_note: 'bootstrap note',
    ...overrides
  };

  if (!('attached_policy_ids' in overrides)) {
    payload.attached_policy_ids = [
      payload.per_tx_policy_id,
      payload.daily_policy_id,
      payload.weekly_policy_id,
      payload.gas_policy_id
    ].filter((value) => typeof value === 'string');
  }

  return payload;
}

test('listAutoGeneratedBootstrapArtifacts reports plaintext and redacted files', async () => {
  const bootstrapArtifacts = await loadModule(`${Date.now()}-list-states`);
  const tempRoot = fs.mkdtempSync(path.join(os.tmpdir(), 'agentpay-bootstrap-artifacts-'));
  fs.chmodSync(tempRoot, 0o700);

  writePrivateJsonFile(
    path.join(tempRoot, 'bootstrap-100-200.json'),
    bootstrapPayload()
  );
  writePrivateJsonFile(
    path.join(tempRoot, 'bootstrap-300-400.json'),
    bootstrapPayload({
      agent_auth_token: '<redacted>',
      agent_auth_token_redacted: true
    })
  );
  writePrivateJsonFile(
    path.join(tempRoot, 'notes.json'),
    { hello: 'world' }
  );

  const result = bootstrapArtifacts.listAutoGeneratedBootstrapArtifacts({
    resolveAgentPayHome: () => tempRoot
  });

  assert.equal(result.length, 2);
  assert.deepEqual(
    result.map((entry) => [path.basename(entry.path), entry.status]),
    [
      ['bootstrap-300-400.json', 'redacted'],
      ['bootstrap-100-200.json', 'plaintext']
    ]
  );
  assert.equal(result[0].error, null);
  assert.equal(result[1].error, null);
  assert.equal(result[0].leaseExpired, false);
  assert.equal(result[1].leaseExpired, false);
});

test('inspectBootstrapArtifact reports invalid and untrusted bootstrap files', async () => {
  const bootstrapArtifacts = await loadModule(`${Date.now()}-invalid-untrusted`);
  const tempRoot = fs.mkdtempSync(path.join(os.tmpdir(), 'agentpay-bootstrap-artifacts-'));
  fs.chmodSync(tempRoot, 0o700);

  const invalidPath = path.join(tempRoot, 'bootstrap-500-600.json');
  writePrivateJsonFile(invalidPath, { hello: 'world' });

  const invalidResult = bootstrapArtifacts.inspectBootstrapArtifact(invalidPath);
  assert.equal(invalidResult.status, 'invalid');
  assert.match(
    invalidResult.error ?? '',
    /lease_id is required|must contain a JSON object|policy_attachment is required|missing token_policies and legacy policy ids/
  );

  if (process.platform !== 'win32') {
    const symlinkTarget = path.join(tempRoot, 'real-bootstrap.json');
    writePrivateJsonFile(symlinkTarget, bootstrapPayload());
    const symlinkPath = path.join(tempRoot, 'bootstrap-700-800.json');
    fs.symlinkSync(symlinkTarget, symlinkPath);

    const untrustedResult = bootstrapArtifacts.inspectBootstrapArtifact(symlinkPath);
    assert.equal(untrustedResult.status, 'untrusted');
    assert.match(untrustedResult.error ?? '', /must not be a symlink/);
  }
});

