test('cleanupAutoGeneratedBootstrapArtifacts reports failed redaction cleanup when fallback delete also fails', async () => {
  const bootstrapArtifacts = await loadModule(`${Date.now()}-cleanup-redact-failure`);
  const tempRoot = fs.mkdtempSync(path.join(os.tmpdir(), 'agentpay-bootstrap-artifacts-'));
  fs.chmodSync(tempRoot, 0o700);

  const plaintextPath = path.join(tempRoot, 'bootstrap-100-200.json');
  writePrivateJsonFile(plaintextPath, bootstrapPayload());

  const originalRenameSync = fs.renameSync;
  const originalRmSync = fs.rmSync;
  fs.renameSync = () => {
    throw new Error('rename failed');
  };
  fs.rmSync = (targetPath, ...args) => {
    if (path.resolve(String(targetPath)) === plaintextPath) {
      throw new Error('delete failed');
    }
    return originalRmSync.call(fs, targetPath, ...args);
  };

  try {
    const result = bootstrapArtifacts.cleanupAutoGeneratedBootstrapArtifacts('redacted', {
      resolveAgentPayHome: () => tempRoot,
    });

    assert.equal(result.action, 'redacted');
    assert.equal(result.files.length, 1);
    assert.equal(result.files[0].cleanup, 'failed');
    assert.match(result.files[0].error ?? '', /rename failed/);
    assert.match(result.files[0].error ?? '', /fallback delete also failed: delete failed/);
  } finally {
    fs.renameSync = originalRenameSync;
    fs.rmSync = originalRmSync;
  }

  assert.equal(fs.existsSync(plaintextPath), true);
});

test('listAutoGeneratedBootstrapArtifacts fails closed for untrusted homes and tolerates missing directories', async () => {
  const bootstrapArtifacts = await loadModule(`${Date.now()}-list-home-edge-cases`);
  const tempRoot = fs.mkdtempSync(path.join(os.tmpdir(), 'agentpay-bootstrap-artifacts-'));
  fs.chmodSync(tempRoot, 0o700);

  if (process.platform !== 'win32') {
    const realHome = path.join(tempRoot, 'real-home');
    fs.mkdirSync(realHome, { recursive: true, mode: 0o700 });
    fs.chmodSync(realHome, 0o700);
    const symlinkHome = path.join(tempRoot, 'home-symlink');
    fs.symlinkSync(realHome, symlinkHome);
    assert.deepEqual(
      bootstrapArtifacts.listAutoGeneratedBootstrapArtifacts({
        resolveAgentPayHome: () => symlinkHome,
      }),
      [],
    );
  }

  const missing = bootstrapArtifacts.listAutoGeneratedBootstrapArtifacts({
    resolveAgentPayHome: () => tempRoot,
    readdirSync: () => {
      const error = new Error('missing home');
      error.code = 'ENOENT';
      throw error;
    },
  });
  assert.deepEqual(missing, []);

  assert.throws(
    () =>
      bootstrapArtifacts.listAutoGeneratedBootstrapArtifacts({
        resolveAgentPayHome: () => tempRoot,
        readdirSync: () => {
          throw new Error('readdir boom');
        },
      }),
    /readdir boom/,
  );
});

test('inspectBootstrapArtifact marks invalid lease metadata and malformed credential fields', async () => {
  const bootstrapArtifacts = await loadModule(`${Date.now()}-inspect-invalid-lease-and-credentials`);
  const tempRoot = fs.mkdtempSync(path.join(os.tmpdir(), 'agentpay-bootstrap-artifacts-'));
  fs.chmodSync(tempRoot, 0o700);

  const invalidLeasePath = path.join(tempRoot, 'bootstrap-1300-1400.json');
  writePrivateJsonFile(
    invalidLeasePath,
    bootstrapPayload({
      lease_expires_at: 'not-an-iso-timestamp',
    }),
  );
  const leaseResult = bootstrapArtifacts.inspectBootstrapArtifact(invalidLeasePath);
  assert.equal(leaseResult.status, 'invalid');
  assert.equal(leaseResult.agentKeyId, '00000000-0000-0000-0000-000000000001');
  assert.equal(leaseResult.leaseExpiresAt, 'not-an-iso-timestamp');
  assert.match(leaseResult.error ?? '', /valid ISO-8601 timestamp/);

  const invalidCredentialPath = path.join(tempRoot, 'bootstrap-1500-1600.json');
  writePrivateJsonFile(
    invalidCredentialPath,
    bootstrapPayload({
      agent_auth_token: '',
    }),
  );
  const credentialsResult = bootstrapArtifacts.inspectBootstrapArtifact(invalidCredentialPath);
  assert.equal(credentialsResult.status, 'invalid');
  assert.equal(credentialsResult.agentKeyId, '00000000-0000-0000-0000-000000000001');
  assert.match(credentialsResult.error ?? '', /agent_auth_token is required/);
});

test('bootstrap artifact helpers use the default AGENTPAY_HOME when deps are omitted', async () => {
  const bootstrapArtifacts = await loadModule(`${Date.now()}-default-agentpay-home`);
  const tempRoot = fs.mkdtempSync(path.join(os.tmpdir(), 'agentpay-bootstrap-artifacts-'));
  const originalAgentPayHome = process.env.AGENTPAY_HOME;
  fs.chmodSync(tempRoot, 0o700);

  try {
    process.env.AGENTPAY_HOME = tempRoot;
    const bootstrapPath = path.join(tempRoot, 'bootstrap-1700-1800.json');
    writePrivateJsonFile(bootstrapPath, bootstrapPayload());

    const listed = bootstrapArtifacts.listAutoGeneratedBootstrapArtifacts();
    assert.equal(listed.length, 1);
    assert.equal(listed[0].status, 'plaintext');

    const cleaned = bootstrapArtifacts.cleanupAutoGeneratedBootstrapArtifacts('deleted');
    assert.equal(cleaned.files.length, 1);
    assert.equal(cleaned.files[0].cleanup, 'deleted');
    assert.equal(fs.existsSync(bootstrapPath), false);
  } finally {
    if (originalAgentPayHome === undefined) {
      delete process.env.AGENTPAY_HOME;
    } else {
      process.env.AGENTPAY_HOME = originalAgentPayHome;
    }
    fs.rmSync(tempRoot, { recursive: true, force: true });
  }
});
