test('repairWalletState covers overwrite migrations and default dependency fallbacks', async () => {
  const walletRepair = await loadModule(`${Date.now()}-overwrite-and-defaults`);

  const overwritten = walletRepair.repairWalletState(
    {
      overwriteKeychain: true,
    },
    {
      readConfig: () => ({
        agentKeyId: TEST_AGENT_KEY_ID,
        agentAuthToken: 'legacy-token',
        chains: {},
      }),
      getWalletStatus: (() => {
        let calls = 0;
        return () => {
          calls += 1;
          return calls === 1
            ? createStatus(['legacy plaintext config token detected'])
            : createStatus([]);
        };
      })(),
      migrateLegacyAgentAuthToken: () => ({
        agentKeyId: TEST_AGENT_KEY_ID,
        source: 'config',
        keychain: {
          service: 'agentpay-agent-auth-token',
          stored: true,
          overwritten: true,
          alreadyPresent: false,
          matchedExisting: false,
        },
        config: {
          agentKeyId: TEST_AGENT_KEY_ID,
        },
      }),
      cleanupAutoGeneratedBootstrapArtifacts: (action) => ({
        agentpayHome: '/tmp/agentpay-home',
        action,
        files: [],
      }),
    },
  );

  assert.equal(overwritten.legacyAgentAuth.action, 'migrated');
  assert.match(overwritten.legacyAgentAuth.reason ?? '', /replaced a different macOS Keychain token/);

  const configModule = await import(`${configModulePath.href}?case=${Date.now()}-defaults`);
  const tempRoot = fs.mkdtempSync(path.join(os.tmpdir(), 'agentpay-wallet-repair-defaults-'));
  process.env.AGENTPAY_HOME = tempRoot;

  try {
    configModule.writeConfig({
      agentKeyId: TEST_AGENT_KEY_ID,
      chains: {},
    });

    const result = walletRepair.repairWalletState();
    assert.equal(result.legacyAgentAuth.action, 'none');
    assert.equal(result.bootstrapArtifacts.error, null);
    assert.deepEqual(result.bootstrapArtifacts.files, []);
    assert.match(
      walletRepair.formatWalletRepairText(result),
      /bootstrap artifacts: no auto-generated bootstrap files found/,
    );
  } finally {
    delete process.env.AGENTPAY_HOME;
    fs.rmSync(tempRoot, { recursive: true, force: true });
  }
});

