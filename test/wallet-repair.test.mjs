test('repairWalletState fails closed on migration conflicts but still cleans bootstrap artifacts', async () => {
  const walletRepair = await loadModule(`${Date.now()}-migrate-conflict`);
  let cleanupCalls = 0;

  const result = walletRepair.repairWalletState(
    {},
    {
      readConfig: () => ({
        agentKeyId: TEST_AGENT_KEY_ID,
        agentAuthToken: 'legacy-token',
        chains: {},
      }),
      getWalletStatus: (() => {
        let calls = 0;
        return () => {
          calls += 1;
          return calls === 1
            ? createStatus([
                'legacy agentAuthToken in config.json differs from the macOS Keychain token for the configured agentKeyId; avoid `--allow-legacy-agent-auth-source` until migrated',
                'auto-generated bootstrap file still contains a plaintext agent auth token',
              ])
            : createStatus([
                'legacy agentAuthToken in config.json differs from the macOS Keychain token for the configured agentKeyId; avoid `--allow-legacy-agent-auth-source` until migrated',
              ]);
        };
      })(),
      migrateLegacyAgentAuthToken: () => {
        throw new Error(
          'macOS Keychain already contains a different agent auth token for this agentKeyId; rerun with --overwrite-keychain after verifying the correct credential',
        );
      },
      cleanupAutoGeneratedBootstrapArtifacts: (action) => {
        cleanupCalls += 1;
        return {
          agentpayHome: '/tmp/agentpay-home',
          action,
          files: [],
        };
      },
    },
  );

  assert.equal(cleanupCalls, 1);
  assert.equal(result.legacyAgentAuth.action, 'skipped');
  assert.match(
    result.legacyAgentAuth.reason ?? '',
    /already contains a different agent auth token/,
  );
  assert.deepEqual(result.fixedWarnings, [
    'auto-generated bootstrap file still contains a plaintext agent auth token',
  ]);
  assert.deepEqual(result.remainingWarnings, [
    'legacy agentAuthToken in config.json differs from the macOS Keychain token for the configured agentKeyId; avoid `--allow-legacy-agent-auth-source` until migrated',
  ]);
});

