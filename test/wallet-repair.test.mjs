test('repairWalletState migrates legacy config secrets and deletes lingering bootstrap artifacts', async () => {
  const walletRepair = await loadModule(`${Date.now()}-success`);
  const beforeWarnings = [
    'legacy agentAuthToken is duplicated in config.json and macOS Keychain; run `agentpay config agent-auth migrate` to scrub plaintext config storage',
    'auto-generated bootstrap file still contains a plaintext agent auth token',
  ];

  let statusCalls = 0;
  let migrateInput = null;
  let cleanupAction = null;

  const result = walletRepair.repairWalletState(
    {},
    {
      readConfig: () => ({
        agentKeyId: TEST_AGENT_KEY_ID,
        agentAuthToken: 'legacy-token',
        chains: {},
      }),
      getWalletStatus: () => {
        statusCalls += 1;
        return statusCalls === 1 ? createStatus(beforeWarnings) : createStatus([]);
      },
      migrateLegacyAgentAuthToken: (input) => {
        migrateInput = input;
        return {
          agentKeyId: TEST_AGENT_KEY_ID,
          source: 'config',
          keychain: {
            service: 'agentpay-agent-auth-token',
            stored: true,
            overwritten: false,
            alreadyPresent: false,
            matchedExisting: false,
          },
          config: {
            agentKeyId: TEST_AGENT_KEY_ID,
          },
        };
      },
      cleanupAutoGeneratedBootstrapArtifacts: (action) => {
        cleanupAction = action;
        return {
          agentpayHome: '/tmp/agentpay-home',
          action,
          files: [
            {
              path: '/tmp/agentpay-home/bootstrap-123-456.json',
              status: 'plaintext',
              agentKeyId: TEST_AGENT_KEY_ID,
              leaseExpiresAt: '2099-01-01T00:00:00Z',
              error: null,
              cleanup: 'deleted',
            },
          ],
        };
      },
    },
  );

  assert.deepEqual(migrateInput, {
    agentKeyId: undefined,
    overwriteKeychain: undefined,
  });
  assert.equal(cleanupAction, 'deleted');
  assert.equal(result.legacyAgentAuth.action, 'migrated');
  assert.match(result.legacyAgentAuth.reason ?? '', /moved into macOS Keychain/);
  assert.equal(result.bootstrapArtifacts.error, null);
  assert.equal(result.bootstrapArtifacts.files.length, 1);
  assert.deepEqual(result.fixedWarnings, beforeWarnings);
  assert.deepEqual(result.remainingWarnings, []);
  assert.deepEqual(result.newWarnings, []);
});

